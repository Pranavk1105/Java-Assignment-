//WAP to demonstrate relational operators in java.

class relationaloperators {
    public static void main(String [] args){
        int a = 34;
        int b = 32;
        System.out.println(" a > b :" + (a>b) );
        System.out.println("a < b :" + (a<b));
        System.out.println( "a == b : " +(a==b));
        System.out.println( " a!=b : " +(a!=b ));
    }

}

