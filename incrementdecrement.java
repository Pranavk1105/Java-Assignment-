class incrementdecrement {
    public static void main (String [] args) {
        int a = 45;
        System.out.println(" pre-increment operator is " + (++a));
        System.out.println(" post-increament operator is " +(a++));
        System.out.println(" pre-decreament operator is " + (--a));
        System.out.println(" post-decreament operator is " + (a--));
    }
}